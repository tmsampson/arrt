// -----------------------------------------------------------------------------------------

use super::vector::Vec3;

// -----------------------------------------------------------------------------------------

pub struct Ray {
    pub origin: Vec3,
    pub direction: Vec3,
}

// -----------------------------------------------------------------------------------------
// Constructor
impl Ray {
    pub fn new(origin: Vec3, direction: Vec3) -> Ray {
        Ray { origin, direction }
    }
}

// -----------------------------------------------------------------------------------------

impl Ray {
    pub fn get_point(&self, t: f32) -> Vec3 {
        self.origin + (self.direction * t)
    }
}
